package com.shinseokki.puzzle.dto;

public class Keyword {
	
		private int K_NUM;
		private String K_NAME;
		private String K_GROUP;
		private int K_COUNT;
		
		
		
		public int getK_NUM() {
			return K_NUM;
		}
		public void setK_NUM(int k_NUM) {
			K_NUM = k_NUM;
		}
		public String getK_NAME() {
			return K_NAME;
		}
		public void setK_NAME(String k_NAME) {
			K_NAME = k_NAME;
		}
		public String getK_GROUP() {
			return K_GROUP;
		}
		public void setK_GROUP(String k_GROUP) {
			K_GROUP = k_GROUP;
		}
		public int getK_COUNT() {
			return K_COUNT;
		}
		public void setK_COUNT(int k_COUNT) {
			K_COUNT = k_COUNT;
		}
		
		
		
	}


